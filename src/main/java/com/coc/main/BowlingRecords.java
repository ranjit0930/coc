package com.coc.main;

public class BowlingRecords {
    public double ballNo;
    public double runsScored;
    public double wicketTaken;
    public double extraNb;
    public double extraWb;
    public double exraRunOverThrow;
    public double outType;

    public double getBallNo() {
        return ballNo;
    }

    public void setBallNo(double ballNo) {
        this.ballNo = ballNo;
    }

    public double getRunsScored() {
        return runsScored;
    }

    public void setRunsScored(double runsScored) {
        this.runsScored = runsScored;
    }

    public double getWicketTaken() {
        return wicketTaken;
    }

    public void setWicketTaken(double wicketTaken) {
        this.wicketTaken = wicketTaken;
    }

    public double getExtraNb() {
        return extraNb;
    }

    public void setExtraNb(double extraNb) {
        this.extraNb = extraNb;
    }

    public double getExtraWb() {
        return extraWb;
    }

    public void setExtraWb(double extraWb) {
        this.extraWb = extraWb;
    }

    public double getExraRunOverThrow() {
        return exraRunOverThrow;
    }

    public void setExraRunOverThrow(double exraRunOverThrow) {
        this.exraRunOverThrow = exraRunOverThrow;
    }

    public double getOutType() {
        return outType;
    }

    public void setOutType(double outType) {
        this.outType = outType;
    }


}
